import UIKit

struct AvenirFont {
    func getFont(size:CGFloat) -> UIFont {
       return UIFont(name:  "AvenirNext-Regular", size: size)!
    }
}


struct HelveticaLight {
    func getFont(size:CGFloat) -> UIFont {
        return UIFont(name: "Helvetica-Light", size: size)!
    }
}


